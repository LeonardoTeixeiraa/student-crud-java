    private void preencherTabela() {
        DefaultTableModel modelo = (DefaultTableModel) tabelaTransporte.getModel();
        modelo.setRowCount(0);

        PontoEmbarqueDAO dao = new PontoEmbarqueDAO();
        List<Object[]> linhas = dao.listarTransportesComPontos();

        for (Object[] linha : linhas) {
            modelo.addRow(linha); 
        }
    }

