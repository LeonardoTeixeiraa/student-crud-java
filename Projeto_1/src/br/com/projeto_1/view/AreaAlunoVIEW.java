    private void btnInscreverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInscreverActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tabelaTransporte.getSelectedRow();
        if (linhaSelecionada >= 0) {
            int idTransporte = Integer.parseInt(tabelaTransporte.getValueAt(linhaSelecionada, 0).toString());

            InscricaoDTO inscricaoDTO = new InscricaoDTO();
            inscricaoDTO.setId_transporte(idTransporte);
            inscricaoDTO.setProntuario(alunoDTO.getProntuario());

            InscricaoCTR inscricaoCTR = new InscricaoCTR();
            boolean sucesso = inscricaoCTR.registrarInscricao(inscricaoDTO);

            if (sucesso) {
                JOptionPane.showMessageDialog(null, "Inscricao confirmada!");
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao confirmar Inscricao!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um transporte para se inscrever.");
        }
    }//GEN-LAST:event_btnInscreverActionPerformed

